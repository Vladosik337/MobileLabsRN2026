export const generateNewsData = (startId = 1, count = 20) => {
  return Array.from({ length: count }, (_, index) => ({
    id: startId + index,
    title: titles[(startId + index - 1) % titles.length],
    description: descriptions[(startId + index - 1) % descriptions.length],
    image: `https://picsum.photos/seed/${startId + index}/400/200`,
    date: new Date(2024, Math.floor(Math.random() * 12), Math.floor(Math.random() * 28) + 1)
      .toLocaleDateString('uk-UA', { day: 'numeric', month: 'long', year: 'numeric' }),
  }));
};
